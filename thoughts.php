<h3 class="title"><a href="#top" name="summer">The Spirituality of Summer</a></h3>
<p>
"Summer afternoon - summer afternoon; to me those have always been the two most 
beautiful words in the English language." So wrote Henry James in one of his 
novels. Summer has a special significance. During summer Sundays, UU people are 
unleashed on the world. Some UU churches are led by lay people during the 
summer; some churches close entirely. I once read a joke; it said:
</p>
<blockquote><p>&quot; Unitarians are the only group God can trust to be out of his 
