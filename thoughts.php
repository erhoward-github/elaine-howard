</blockquote>
<p>
Alright then, what causes the seasons -summer - in particular? Our distance 
from the sun does change over the course of the year, but this does not have a 
very large affect. The earth, it seems, is tilted relative to the plane of its 
orbit around the sun. During this time of the year, the northern hemisphere is 
pointed more towards the sun and receives more light. In the winter it is 
tilted away from the sun and receives less light. Of course, the seasons are 
reversed for the southern hemisphere. The perturbations and eccentricities of 
the cosmos have conspired to give us a many of our traditions and rituals. What 
if the earth's pirouette was perfectly perpendicular? What if the earth's 
circumnavigation of the sun was certainly circular? Then life on earth might be 
a bit more bland. We are so small, yet we are intimately related to something 
so big - the universe.
</p>
<p>
Life abounds around us. The trees sway in the wind. The grass grows longer and 
longer - much to the chagrin of homeowners. The birds swoop from above. The 
bunny rabbits, squirrels and fellow humans scurry to their destinations. The 
beautiful flowers bloom. But what if one doesn't feel connected with this 
explosion of life? What if one's spirit is ebbing rather than overflowing? In 
one of his sonnets, Shakespeare wrote:
</p>
<blockquote><p>&quot; The summer's flower is to the summer sweet, Though to itself it 
	only live and die.&quot;</p>
</blockquote>
<p>
Life can be very difficult. There are work problems and family problems and 
relationship problems and world problems and problems I never could have 
imagined. There can be many irritations large and small, so much so that life 
itself seems to be the problem. Despair of this magnitude overwhelms one's 
abilty to cope with the pain. This is a difficult problem; I have felt this. 
Under the sun's direct light, we are not frozen in place as we might be in 
winter. We can move and reach out. What I've realized is that other people have 
faced similar difficulties and survived. I realized that sometimes I must give 
myself distance from feelings of despair - like water off a duck's back. I 
realized that the feeling of relief and release can only occur if one is alive 
and connected. And I've realized that spiritual health is an ongoing process 
and must be cultivated.
</p>
<p>
Perhaps this is why UUs relax during the summer. We get a chance to live and 
practice what we preach and experience all the things we've learned during the 
rest of the year.
</p>
